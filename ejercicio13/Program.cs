public class ejercicio13
{
    public static void Main(String[] args)
    {
        Console.WriteLine("Ingrese un numero \n");
        int num = int.Parse(Console.ReadLine());

        switch (num)
        {
            case 1:
                Console.WriteLine("\n Uno");
                break;
            case 2:
                Console.WriteLine("\n Dos");
                break;
            case 3:
                Console.WriteLine("\n Tres");
                break;
            case 4:
                Console.WriteLine("\n Cuatro");
                break;
            case 5:
                Console.WriteLine("Cinco");
                break;
        }
               

    }

}
